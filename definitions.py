# Useful constant to represent an "infinity" but is only about ~1000x
# the largest value we would use (to prevent numeric overflows).

kUnlimitedTime: int = 100000000000000000
kNanosPerSecond: int = 1000000000
kNanosPerMicro: int = 1000
kPi: int = 3.14159265359
kE: int = 2.71828182846

# Fitness bounds.

kMinFitness: int = 0.0
kMaxFitness: int = 1.0
